export const getCategories = () => {
  const products = getProducts();
  return [
    { slug: "bunk-beds", name: "Bunk Beds", count: products.filter(p => p.category === "bunk-beds").length },
    { slug: "loft-beds", name: "Loft Beds", count: products.filter(p => p.category === "loft-beds").length },
    { slug: "single-beds", name: "Single Beds", count: products.filter(p => p.category === "single-beds").length },
    { slug: "mattresses", name: "Mattresses", count: products.filter(p => p.subcategory === "mattresses").length },
    { slug: "accessories", name: "Accessories", count: products.filter(p => p.category === "accessories" && p.subcategory !== "mattresses").length }
  ];
};

export const getRelatedProducts = (category: string, excludeId: string): Product[] => {
  const products = getProducts();
  return products.filter(p => p.category === category && p.id !== excludeId).slice(0, 8);
};

