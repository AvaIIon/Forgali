// Load products from service layer
export const getProducts = (): Product[] => {
  return loadProducts();
};

export const getProductsByCategory = (category: string): Product[] => {
  const products = getProducts();
  // Special handling for mattresses - they're in accessories but subcategorized as mattresses
  if (category === "mattresses") {
    return products.filter((product) => product.subcategory === "mattresses");
  }
  return products.filter((product) => product.category === category);
};

export const getProductsBySubcategory = (category: string, subcategory: string): Product[] => {
  const products = getProducts();
  return products.filter((product) => product.category === category && product.subcategory === subcategory);
};

export const getSubcategories = (category: string) => {
  const products = getProducts();
  const subcategories = [...new Set(products.filter(p => p.category === category).map(p => p.subcategory))];
  return subcategories.map(sub => ({
    slug: sub,
    name: sub.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' '),
    count: products.filter(p => p.category === category && p.subcategory === sub).length,
  }));
};

export const getProductById = (id: string): Product | undefined => {
  const products = getProducts();
  return products.find((product) => product.id === id);
};

